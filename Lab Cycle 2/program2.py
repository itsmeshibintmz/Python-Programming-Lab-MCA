# Write a program to determine whether the character entered is a vowel or not.
character=input("Enter a character\n")
if character=='a' or character=='e' or character=='i' or character=='o' or character=='u' or character=='A' or character=='E' or character=='I' or character=='O' or character=='U':
    print("The entered character is a Vowel")
else:
    print("The entered character is not a vowel")