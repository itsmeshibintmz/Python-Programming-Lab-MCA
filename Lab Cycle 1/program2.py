""" Program to demonstrate different number
    data types in python """

# Integer value
integerValue=3
print("Integer value is",integerValue)
# Printing the type of integer value
print("Type of integer value is",type(integerValue))
# Printing the id of integer value
print("ID of integer value is",id(integerValue))

# Floating point value
floatValue=3.14
print("Float value is",floatValue)
# Printing the type of float value
print("Type of float value is",type(floatValue))
# Printing the id of float value
print("ID of float value is",id(floatValue))

# Complex value
complexValue=3+4j
print("Complex value is",complexValue)
# Printing the type of complex value
print("Type of complex value is",type(complexValue))
# Printing the id of complex value
print("ID of complex value is",id(complexValue))

