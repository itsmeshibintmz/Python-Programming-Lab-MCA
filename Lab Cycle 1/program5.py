# Program to perform Arithmetic Operations 

# Getting the inputs from the user
number1=int(input("Enter first integer number\n"))
number2=int(input("Enter second integer number\n"))

# Performing the Arithmetic Operations and displaying the result
print("Sum of two numbers is",number1+number2)
print("Difference of two numbers is",number1-number2)
print("Product of two numbers is",number1*number2)
print("Division of two numbers is",number1/number2)
print("Modulus of two numbers is",number1%number2)
print("Exponent of two numbers is",number1**number2)

